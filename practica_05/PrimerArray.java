class PrimerArray{
   public static void main(String arg[]){
      String nombres[] ={
          "Juan",
          "Maria",
          "Antonio",
          "Victoria"
      };
      nombres[2]="Alexis";
      System.out.println(nombres[0]);
      System.out.println(nombres[1]);
      System.out.println(nombres[2]);
      System.out.println(nombres[3]);
   }
}